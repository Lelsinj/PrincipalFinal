    public void excCorretor() throws CRECIException{
        Corretor corretor = new Corretor();
        String tamanhoTextCRECI = textCRECIEx.getText();
        if (tamanhoTextCRECI.length() == 0) {
            throw new CRECIException();
        }
        corretor.setCRECI(tamanhoTextCRECI);
        
        corretor = comandos.exCorretorCRECI(corretor);
        
        if(corretor == null){

            JOptionPane.showMessageDialog(
                    null,
                    "Corretor REMOVIDO com sucesso!",
                    "Exclus찾o de Corretor",
                    JOptionPane.INFORMATION_MESSAGE
            );
        }
        else{
            JOptionPane.showMessageDialog(
                    null,
                    "N찾o existe corretor com este CRECI cadastrado!",
                    "Exclus찾o de Corretor",
                    JOptionPane.ERROR_MESSAGE
            );  
        }
        textCRECIEx.setText("");
        
    }
    
    public void popularTbCorretor(){
    DefaultTableModel modelo = (DefaultTableModel) tableCorretores.getModel();
    modelo.setRowCount(0); 
    
    for (Corretor corretor : comandos.getBdCorretores()) {
        modelo.addRow(new Object[]{corretor.getNome(), corretor.getCRECI()});
    }
    }
    
    public void voltarHome(){
        int ret = JOptionPane.showConfirmDialog(
                null,
                "Deseja realmente voltar ao menu principal?",
                "Saida",
                JOptionPane.YES_NO_CANCEL_OPTION
        );
        if(ret == 0){
            dispose();
        }
    }
